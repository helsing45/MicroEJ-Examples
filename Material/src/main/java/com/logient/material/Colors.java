package com.logient.material;

/**
 *
 */
public class Colors {
	public int primary;
	public int onPrimary;
	public int primaryVariant;
	public int secondary;
	public int onSecondary;
	public int secondaryVariant;
	public int background;
	public int onBackground;
	public int surface;
	public int onSurface;
	public int error;
	public int onError;

	/**
	 * @param primary
	 * @param onPrimary
	 * @param primaryVariant
	 * @param secondary
	 * @param onSecondary
	 * @param secondaryVariant
	 * @param background
	 * @param onBackground
	 * @param surface
	 * @param onSurface
	 * @param error
	 * @param onError
	 */
	public Colors(int primary, int onPrimary, int primaryVariant, int secondary, int onSecondary, int secondaryVariant,
			int background, int onBackground, int surface, int onSurface, int error, int onError) {
		super();
		this.primary = primary;
		this.onPrimary = onPrimary;
		this.primaryVariant = primaryVariant;
		this.secondary = secondary;
		this.onSecondary = onSecondary;
		this.secondaryVariant = secondaryVariant;
		this.background = background;
		this.onBackground = onBackground;
		this.surface = surface;
		this.onSurface = onSurface;
		this.error = error;
		this.onError = onError;
	}
}
